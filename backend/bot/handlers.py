from bot.utils import set_commands


async def get_start(message: Message, bot: Bot):
    await set_commands(bot)
    print(message.from_user.id)
    print(type(message.from_user.id))
    web_app_register = WebAppInfo(url=f"https://www.pannybulochka.ru/register/{message.chat.id}")
    main_kb = InlineKeyboardMarkup(
        inline_keyboard=[
            [
                InlineKeyboardButton(text='–∑–Ω–∞–∫–æ–º—Å—Ç–≤–æ'.upper(), web_app=web_app_register)
            ],
        ]
    )
    await message.answer(
        f"–ü—Ä–∏–≤–µ—Ç, –Ω–∞–º –æ—á–µ–Ω—å –≤–∞–∂–Ω–æ –ø–æ–∑–Ω–∞–∫–æ–º–∏—Ç—å—Å—è —Å —Ç–æ–±–æ–π, —á—Ç–æ–±—ã —Ç—ã –º–æ–≥"
        f"–≤–æ—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å—Å—è –Ω–∞—à–∏–º –ø–æ–¥–∞—Ä–∫–æ–º –∏ –ø–æ–ª—É—á–∞—Ç—å —Å–∞–º—ã–µ –≥–æ—Ä—è—á–∏–µ –∞–∫—Ü–∏–∏ –∏ –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏—è üî•üî•",
        reply_markup=main_kb)


